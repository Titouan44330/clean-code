package ex1;

public class Calcul {

    /**
     * @param a
     * @param b
     * @return sum of a and b
     */
    public int getSum(int a, int b) {
        return a + b;
    }

    /**
     * @param a
     * @param b
     * @return dif of a and b
     */
    public int getDif(int a, int b) {
        return a - b;
    }
}
